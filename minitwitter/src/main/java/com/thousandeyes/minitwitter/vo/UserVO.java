package com.thousandeyes.minitwitter.vo;


public class UserVO {
	
	private String user;
	
	private String followUser;
	
	private String unfollowUser;
	

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getFollowUser() {
		return followUser;
	}

	public void setFollowUser(String followUser) {
		this.followUser = followUser;
	}

	public String getUnfollowUser() {
		return unfollowUser;
	}

	public void setUnfollowUser(String unfollowUser) {
		this.unfollowUser = unfollowUser;
	}


}
